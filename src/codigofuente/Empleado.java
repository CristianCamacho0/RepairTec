package codigofuente;

public class Empleado {
	public static int ID_Empleado;
	public static String Tipo_Sangre;
	public static int Salario;
	public static String Horario;
	public static String RFC;
	public static int ID_Sucursal;
	
	public static int getID_Empleado() {
		return ID_Empleado;
	}
	public static void setID_Empleado(int iD_Empleado) {
		ID_Empleado = iD_Empleado;
	}
	public static String getTipo_Sangre() {
		return Tipo_Sangre;
	}
	public static void setTipo_Sangre(String tipo_Sangre) {
		Tipo_Sangre = tipo_Sangre;
	}
	public static int getSalario() {
		return Salario;
	}
	public static void setSalario(int salario) {
		Salario = salario;
	}
	public static String getHorario() {
		return Horario;
	}
	public static void setHorario(String horario) {
		Horario = horario;
	}
	public static String getRFC() {
		return RFC;
	}
	public static void setRFC(String rFC) {
		RFC = rFC;
	}
	public static int getID_Sucursal() {
		return ID_Sucursal;
	}
	public static void setID_Sucursal(int iD_Sucursal) {
		ID_Sucursal = iD_Sucursal;
	}
	

}
