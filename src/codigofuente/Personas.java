package codigofuente;

public class Personas {

	public static String Nombre;
	public static String Curp;
	public static int Num_Telefono;
	public static String Direccion;
	
	public static String getNombre() {
		return Nombre;
	}
	public static void setNombre(String nombre) {
		Nombre = nombre;
	}
	public static String getCurp() {
		return Curp;
	}
	public static void setCurp(String curp) {
		Curp = curp;
	}
	public static int getNum_Telefono() {
		return Num_Telefono;
	}
	public static void setNum_Telefono(int num_Telefono) {
		Num_Telefono = num_Telefono;
	}
	public static String getDireccion() {
		return Direccion;
	}
	public static void setDireccion(String direccion) {
		Direccion = direccion;
	}
	
	
}
