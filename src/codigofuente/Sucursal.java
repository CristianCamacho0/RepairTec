package codigofuente;

public class Sucursal {
	public static int ID_Sucursal;
	public static String Nombre_Sucursal;
	public static String Ciudad;
	public static String Colonia;
	public static int Codigo_Postal;
	public static int Num_Exterior;
	public static String Calle;
	
	public static int getID_Sucursal() {
		return ID_Sucursal;
	}
	public static void setID_Sucursal(int iD_Sucursal) {
		ID_Sucursal = iD_Sucursal;
	}
	public static String getNombre_Sucursal() {
		return Nombre_Sucursal;
	}
	public static void setNombre_Sucursal(String nombre_Sucursal) {
		Nombre_Sucursal = nombre_Sucursal;
	}
	public static String getCiudad() {
		return Ciudad;
	}
	public static void setCiudad(String ciudad) {
		Ciudad = ciudad;
	}
	public static String getColonia() {
		return Colonia;
	}
	public static void setColonia(String colonia) {
		Colonia = colonia;
	}
	public static int getCodigo_Postal() {
		return Codigo_Postal;
	}
	public static void setCodigo_Postal(int codigo_Postal) {
		Codigo_Postal = codigo_Postal;
	}
	public static int getNum_Exterior() {
		return Num_Exterior;
	}
	public static void setNum_Exterior(int num_Exterior) {
		Num_Exterior = num_Exterior;
	}
	public static String getCalle() {
		return Calle;
	}
	public static void setCalle(String calle) {
		Calle = calle;
	}
	

}
